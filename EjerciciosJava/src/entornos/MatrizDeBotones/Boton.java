package entornos.MatrizDeBotones;

public class Boton {
	private int fila;
	private int columna;
	private int valor;
	/**
	 * @param fila
	 * @param columna
	 * @param valor
	 */
	public Boton(int fila, int columna, int valor) {
		super();
		this.fila = fila;
		this.columna = columna;
		this.valor = valor;
	}
	



}
